Pengembangan terhadap sektor swasta merupakan suatu hal yang tidak diragukan lagi perlu untuk dilakukan.
UKM memiliki peran penting dalam pengembangan usaha di Indonesia.UKM juga merupakan cikal bakal dari tumbuhnya usaha besar.
”Hampir semua usaha besar berawal dari UKM.Usaha kecil menengah (UKM) harus terus ditingkatkan (up grade) dan aktif agar dapat maju dan 
bersaing dengan perusahaan besar. Jika tidak, UKM di Indonesia yang merupakan jantung perekonomian Indonesia tidak akan bisa maju dan berkembang.
Satu hal yang perlu diingat dalam pengembangan UKM adalah bahwa langkah ini tidak semata-mata merupakan langkah yang harus diambil oleh 
Pemerintah dan hanya menjadi tanggung jawab Pemerintah.Pihak UKM sendiri sebagai pihak yang dikembangkan, dapat mengayunkan langkah 
bersama-sama dengan Pemerintah.Selain Pemerintah dan UKM, peran dari sektor Perbankan juga sangat penting terkait dengan segala hal 
mengenai pendanaan, terutama dari sisi pemberian pinjaman atau penetapan kebijakan perbankan. Lebih jauh lagi, terkait dengan ketersediaan 
dana atau modal, peran dari para investor baik itu dari dalam maupun luar negeri, tidak dapat pula kita kesampingkan.
Pemerintah pada intinya memiliki kewajiban untuk turut memecahkan tiga hal masalah klasik yang kerap kali menerpa UKM, yakni akses pasar,
 modal, dan teknologi yang selama ini kerap menjadi pembicaraan di seminar atau konferensi. Secara keseluruhan, terdapat beberapa hal yang 
 harus diperhatikan dalam melakukan pengembangan terhadap unit usaha UKM, antara lain kondisi kerja, promosi usaha baru, akses informasi, 
 akses pembiayaan, akses pasar, peningkatan kualitas produk dan SDM, ketersediaan layanan pengembangan usaha, pengembangan cluster, jaringan 
 bisnis, dan kompetisi.
Perlu disadari, UKM berada dalam suatu lingkungan yang kompleks dan dinamis.Jadi, upaya mengembangkan UKM tidak banyak berarti bila tidak
mempertimbangkan pembangunan (khususnya ekonomi) lebih luas. Konsep pembangunan yang dilaksanakan akan membentuk ‘aturan main’ bagi pelaku usaha 
(termasuk UKM) sehingga upaya pengembangan UKM tidak hanya bisa dilaksanakan secara parsial, melainkan harus terintegrasi dengan pembangunan
 ekonomi nasional dan dilaksanakan secara berkesinambungan. Kebijakan ekonomi (terutama pengembangan dunia usaha) yang ditempuh selama ini belum
 menjadikan ikatan kuat bagi, terciptanya keterkaitan antara usaha besar ukm saat ini,Kementerian Koperasi dan Usaha Kecil Menengah berencana untuk
 menciptakan 20 juta usaha kecil menengah baru tahun 2020.Tahun 2020 adalah masa yang menjanjikan begitu banyak peluang karena di tahun tersebut
 akan terwujud apa yang dimimpikan para pemimpin ASEAN yang tertuang dalam Bali Concord II. Suatu komunitas ekonomi ASEAN, yang peredaran produk-produk
 barang dan jasanya tidak lagi dibatasi batas negara, akan terwujud. Kondisi ini membawa sisi positif sekaligus negatif bagi UKM. Menjadi positif
 apabila produk dan jasa UKM mampu bersaing dengan produk dan jasa dari negara-negara ASEAN lainnya, namun akan menjadi negatif apabila sebaliknya.
 Untuk itu, kiranya penting bila pemerintah mendesain program yang jelas dan tepat sasaran serta mencanangkan penciptaan 20 juta UKM sebagai program nasional.
